package main

// Race Detector (детектор гонок) — инструмент, который выявляет гонки данных в параллельных программах.
// Гонки возникают, когда несколько горутин обращаются к общим данным, но не синхронизированы друг с другом:
// одна горутина пытается читать или писать данные, которые в то же время изменяются другой.
//
// Чтобы запустить Race Detector в Go, используйте флаг -race при вызове команд go run, go build или go test.
// Например:
// go run -race имя_файла.go
// После запуска программы или тестов с флагом -race, компилятор Go отслеживает доступ к общим данным
// из разных горутин и выводит предупреждения, если обнаруживает гонки.
